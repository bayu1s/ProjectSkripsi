    private void btnhitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhitungActionPerformed
        // TODO add your handling code here:
        int mtk = Integer.parseInt(txtmtk.getText());
        int indo = Integer.parseInt(txtbindo.getText());
        int eng = Integer.parseInt(txtbeng.getText());
        int ilmukeag = Integer.parseInt(txtilk.getText());
        
        int jumlah = (mtk+indo+eng+ilmukeag);
        txtjml.setText(""+jumlah);
        int rata2 = (mtk+indo+eng+ilmukeag)/4;
        txtrata.setText(""+rata2);
        
        if(rata2>=75){
            txtket.setText("LULUS");
        }else{
            txtket.setText("TIDAK LULUS");
        }
    }//GEN-LAST:event_btnhitungActionPerformed


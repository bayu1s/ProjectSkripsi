      protected void autonumber() {
        try {
            String sql = "SELECT Id_tes FROM hasil_test ORDER BY Id_tes DESC LIMIT 1";
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            txtidtes.setText("T0001");
            while (hasil.next()) {
                System.out.println(hasil.getString("Id_tes").substring(2));
                String id_tes = hasil.getString("Id_tes").substring(2);
                int AN = Integer.parseInt(id_tes) + 1;
                String Nol = "";

                if (AN < 10) {
                    Nol = "000";
                } else if (AN < 100) {
                    Nol = "00";
                } else if (AN < 1000) {
                    Nol = "0";
                } else if (AN < 10000) {
                    Nol = "";
                }

                txtidtes.setText("T" + Nol + AN);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Auto number gagal " + e);
        }
    }
      

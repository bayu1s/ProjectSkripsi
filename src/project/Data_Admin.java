    private void tblstaffMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblstaffMouseClicked
        // TODO add your handling code here:
        DefaultTableModel tabmode = (DefaultTableModel)tblstaff.getModel();
        int bar = tblstaff.getSelectedRow();
        String a = tabmode.getValueAt(bar, 0).toString();
        String b = tabmode.getValueAt(bar, 1).toString();
        String c = tabmode.getValueAt(bar, 2).toString();
        String d = tabmode.getValueAt(bar, 3).toString();
        String e = tabmode.getValueAt(bar, 4).toString();
        String f = tabmode.getValueAt(bar, 5).toString();
        String g = tabmode.getValueAt(bar, 6).toString();
        String h = tabmode.getValueAt(bar, 7).toString();
        String i = tabmode.getValueAt(bar, 8).toString();
        

        txtidstaff.setText(a);
        txtnama.setText(b);
        cmbjk.setSelectedItem(c);
        txttmplhr.setText(d);
        
        java.util.Date thnlhir = null;
        try {
            thnlhir = new SimpleDateFormat("yyyy-MM-dd").parse(e);
        } catch (ParseException ex) {
            Logger.getLogger(Data_Admin.class.getName()).log(Level.SEVERE, null, ex);
        }
        tgllhr.setDate(thnlhir);

        
        txtalamat.setText(f);
        cmbjbtn.setSelectedItem(g);
        txtusername.setText(h);
        txtpass.setText(i);
    }//GEN-LAST:event_tblstaffMouseClicked


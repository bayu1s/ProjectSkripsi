    //ID pendaftaran auto
    protected void autonumber() {
        try {
            String sql = "SELECT Id_staff FROM admin ORDER BY Id_staff DESC LIMIT 1";
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            txtidstaff.setText("S0001");
            while (hasil.next()) {
                System.out.println(hasil.getString("Id_staff").substring(2));
                String id_staff = hasil.getString("Id_staff").substring(2);
                int AN = Integer.parseInt(id_staff) + 1;
                String Nol = "";

                if (AN < 10) {
                    Nol = "000";
                } else if (AN < 100) {
                    Nol = "00";
                } else if (AN < 1000) {
                    Nol = "0";
                } else if (AN < 10000) {
                    Nol = "";
                }

                txtidstaff.setText("S" + Nol + AN);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Auto number gagal " + e);
        }
    }
